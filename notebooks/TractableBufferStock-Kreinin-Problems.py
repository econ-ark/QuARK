# 1. Kreinin (1961) describes a fascinating natural experiment: In the late 1940s, Germany made reparations payments to Jewish Israeli citizens who had fled to Israel from Germany during World War II. For simplicity, suppose that all Israeli recipients of these payments were buffer stock savers, all recipients were employed at the same wage, and all of them had already reached their target level of savings at the time that the surprise one-time reparations payment reached them. Draw diagrams showing what the model predicts about the dynamics of the level of consumption, assets, and consumption growth following the receipt of the payments by Israeli citizens.
#
#
# 2. Now suppose that, while each recipient received the same amount of money, different recipients had very different levels of permanent wages. Specifically, suppose that there are "poor" and "rich" Israelis, and that for the poor ones the reparations payment is, say, a couple of years' worth of permanent income, while for the rich ones the reparations payment is a small fraction of a year's income. How would you expect the marginal propensity to consume (MPC) out of the payments to differ for poor versus rich Israelis? If there is any difference in the MPC's explain it in intuitive terms.
#
#
# 3. Now consider the one-time "economic stimulus checks" that the U.S. government mailed to households in the summer of 2008, which were a small percentage of income or some households and a very small percentage for other households. Assume that these checks were treated by households as one-time windfalls, not to be repeated. Discuss whether the buffer stock theory suggests that the MPC out of these checks should be similar to, or different from, the Israeli experience, and discuss whether the experiences of "poor" or "rich" Israeli households might be more relevant. 
